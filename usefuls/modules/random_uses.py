import random


# RANDOM CHOICE
# Example list of items
items = ["apple", "banana", "cherry", "date"]

# Select a single random item
selected_item = random.choice(items)

# Print the result
print(selected_item)
# END RANDOM CHOICE


# RANDOM INT
# Generates a random integer between 1 and 6, inclusive
dice_roll = random.randint(1, 6)
print(dice_roll)
# END RANDOM INT
